# Exercícios
# Crie funções que duplicam, triplicam e quadruplicam
# o número recebido como parâmetro.
def duplicar(numero):
    return numero * 2

def triplicar(numero):
    return numero * 3

def quadruplicar(numero):
    return numero * 4

print(duplicar(2))  # 4
print(triplicar(2))  # 6
print(quadruplicar(2))  # 8
